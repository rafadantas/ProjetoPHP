	<h1>A EMPRESA</h1>
	
	<p>O projeto ‘O Senhor das Pizzas’ consiste em uma pizzaria que oferece serviço online. Na página principal há um menu onde é possível navegar pelo site, sendo possível visualizar informações sobre a empresa, pesquisar sobre os tipos de pizza que são oferecidos, destaques do site, realizar cadastro, acessar seus pedidos e também uma aba para entrar em contato, tirar dúvidas ou sugestões (este é o modo ‘usuário’). Para o adm, será possível cadastrar e alterar clientes, os tipos de pizzas, sabores e tamanhos, pagamentos, assim como administrar todos os componentes do site.
	</p>
	
